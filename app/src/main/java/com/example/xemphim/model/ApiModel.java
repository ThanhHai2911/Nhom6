package com.example.xemphim.model;

public class ApiModel {
    private String name;
    private String url;

    public ApiModel() {}

    public ApiModel(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}

